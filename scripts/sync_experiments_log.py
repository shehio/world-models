#!/usr/bin/env python3
"""Regenerate EXPERIMENTS_LOG.md from site/content/experiments.md.

The site is the source of truth for the running experiments narrative.
EXPERIMENTS_LOG.md exists so the same content is reviewable in-repo and
on GitHub without rendering Hugo. To keep the two in lockstep, this
script translates the Hugo-flavored Markdown into plain GitHub-flavored
Markdown.

Run from the repo root:

    python scripts/sync_experiments_log.py

CI re-runs it and fails if the result diverges from the committed file.

Transformations:
  - Strip the YAML frontmatter block.
  - Replace Hugo block-attribute anchors `{#id}` with inline
    `<a id="id"></a>` so GitHub still resolves intra-file anchor links.
  - Drop chart shortcodes (`{{< chart-* >}}`) — they're SVG partials
    that only Hugo can render; leave a one-line pointer to the site.
  - Rewrite internal Hugo paths (`/experiments/#search`,
    `/next/`, ...) to absolute GitHub Pages URLs so they don't 404 on
    GitHub.
"""
from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
SOURCE = REPO_ROOT / "site" / "content" / "experiments.md"
TARGET = REPO_ROOT / "EXPERIMENTS_LOG.md"
SITE_BASE = "https://shehio.github.io/world-models"

HEADER = """<!-- AUTO-GENERATED by scripts/sync_experiments_log.py from site/content/experiments.md. Do not edit by hand. -->

"""


def strip_frontmatter(text: str) -> str:
    if not text.startswith("---\n"):
        return text
    end = text.find("\n---\n", 4)
    if end == -1:
        return text
    return text[end + len("\n---\n") :].lstrip("\n")


def rewrite_anchors(text: str) -> str:
    # `## Heading {#id}` -> `## Heading<a id="id"></a>`
    return re.sub(r"\{#([A-Za-z0-9_-]+)\}", r'<a id="\1"></a>', text)


def strip_shortcodes(text: str) -> str:
    # `{{< chart-foo >}}` -> small pointer text
    return re.sub(
        r"\{\{<\s*(chart-[A-Za-z0-9_-]+)\s*>\}\}",
        r"_(interactive \1 chart on the site)_",
        text,
    )


def rewrite_internal_links(text: str) -> str:
    # `](/foo/...)` -> `](https://shehio.github.io/world-models/foo/...)`
    # Only fires on links that begin with a single slash followed by a
    # word char — leaves https://, ./, and # links untouched.
    return re.sub(r"\]\((/[A-Za-z][^)]*)\)", lambda m: f"]({SITE_BASE}{m.group(1)})", text)


def transform(text: str) -> str:
    text = strip_frontmatter(text)
    text = rewrite_anchors(text)
    text = strip_shortcodes(text)
    text = rewrite_internal_links(text)
    return HEADER + text


def main(argv: list[str]) -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument(
        "--check",
        action="store_true",
        help="Exit 1 if regenerating would change the committed file (CI mode).",
    )
    args = ap.parse_args(argv)

    src_text = SOURCE.read_text(encoding="utf-8")
    new_text = transform(src_text)

    if args.check:
        old_text = TARGET.read_text(encoding="utf-8") if TARGET.exists() else ""
        if old_text != new_text:
            sys.stderr.write(
                f"EXPERIMENTS_LOG.md is out of sync with {SOURCE.relative_to(REPO_ROOT)}.\n"
                f"Run `python scripts/sync_experiments_log.py` and commit the result.\n"
            )
            return 1
        return 0

    TARGET.write_text(new_text, encoding="utf-8")
    print(f"wrote {TARGET.relative_to(REPO_ROOT)} ({len(new_text)} bytes)")
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
