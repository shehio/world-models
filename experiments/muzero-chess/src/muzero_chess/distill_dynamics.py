"""Train ONLY the dynamics g_θ to be a faithful latent forward-model of the
frozen teacher, using transition data generated by teacher self-play.

Data: short trajectories (o_0, a_0, o_1, ..., a_{K-1}, o_K) from games where
moves are sampled from the teacher policy (plus epsilon-random for coverage).
No Stockfish, no PGN parsing — generated fresh from the teacher.

Loss (K-step unroll, h + f frozen):
    s_0   = h(o_0)                         # no grad
    s*_k  = h(o_k)                         # no grad — latent target
    ŝ_k   = g(ŝ_{k-1}, a_{k-1})            # ŝ_0 = s_0
    latent = Σ_k MSE(ŝ_k, s*_k)            # dense regression anchor
    pred   = Σ_k [softCE(f(ŝ_k).p, f(s*_k).p) + MSE(f(ŝ_k).v, f(s*_k).v)]
                                            # value-equivalence — the objective we care about
    reward = Σ_k MSE(r̂_k, r_k)            # r_k = 0 except ±1 at terminal
    total  = latent_w·latent + pred_w·pred + reward
Only g.parameters() are optimized.
"""
from __future__ import annotations

import random
from dataclasses import dataclass

import chess
import numpy as np
import torch
import torch.nn.functional as F

from wm_chess.board import decode_move, encode_board, encode_move

from .config import MuZeroConfig


@dataclass
class _Game:
    obs: list[np.ndarray]      # encoded board at each ply, length T
    actions: list[int]         # action idx played at each ply, length T
    rewards: list[float]       # reward AFTER each action, length T (0 except terminal)


@torch.no_grad()
def _teacher_move(teacher, board: chess.Board, cfg: MuZeroConfig,
                  device, rng: random.Random) -> tuple[np.ndarray, int]:
    """Pick a move: epsilon-random for coverage, else sample teacher policy."""
    obs = encode_board(board)
    legal = list(board.legal_moves)
    legal_idx = [encode_move(m, board) for m in legal]

    if rng.random() < cfg.epsilon_random:
        action = rng.choice(legal_idx)
        return obs, action

    logits = teacher(torch.from_numpy(obs).unsqueeze(0).to(device))[0][0]  # (4672,)
    masked = torch.full_like(logits, float("-inf"))
    for a in legal_idx:
        masked[a] = logits[a]
    probs = torch.softmax(masked, dim=-1).cpu().numpy()
    action = int(np.random.choice(len(probs), p=probs))
    return obs, action


def generate_transitions(teacher, cfg: MuZeroConfig, n_games: int,
                         *, device=None, seed: int | None = None) -> list[_Game]:
    """Play n_games of teacher self-play; return per-game (obs, action, reward)."""
    device = device or torch.device("cpu")
    rng = random.Random(seed)
    games: list[_Game] = []
    for _ in range(n_games):
        board = chess.Board()
        obs_list: list[np.ndarray] = []
        act_list: list[int] = []
        while not board.is_game_over(claim_draw=True) and len(act_list) < cfg.max_plies:
            obs, action = _teacher_move(teacher, board, cfg, device, rng)
            move = decode_move(action, board)
            if move not in board.legal_moves:        # decode guard (rare)
                move = rng.choice(list(board.legal_moves))
                action = encode_move(move, board)
            obs_list.append(obs)
            act_list.append(action)
            board.push(move)
        rewards = [0.0] * len(act_list)
        outcome = board.outcome(claim_draw=True)
        if outcome is not None and outcome.termination == chess.Termination.CHECKMATE:
            rewards[-1] = 1.0          # mover-perspective reward at the mating move
        if act_list:
            games.append(_Game(obs=obs_list, actions=act_list, rewards=rewards))
    return games


class TransitionBuffer:
    """Holds games; samples K-step windows that fit fully inside a game."""

    def __init__(self, K: int):
        self.K = K
        self._games: list[_Game] = []

    def add(self, games: list[_Game]) -> None:
        # Only keep games long enough to yield at least one full K+1 window.
        self._games.extend(g for g in games if len(g.actions) > self.K)

    def __len__(self) -> int:
        return len(self._games)

    @property
    def n_windows(self) -> int:
        return sum(len(g.actions) - self.K for g in self._games)

    def sample(self, batch_size: int, rng: random.Random | None = None) -> dict:
        r = rng or random
        K = self.K
        obs_seq = np.empty((batch_size, K + 1, 19, 8, 8), dtype=np.float32)
        actions = np.empty((batch_size, K), dtype=np.int64)
        rewards = np.zeros((batch_size, K), dtype=np.float32)
        for b in range(batch_size):
            g = r.choice(self._games)
            t = r.randint(0, len(g.actions) - K - 1)   # window [t, t+K] fits
            for k in range(K + 1):
                obs_seq[b, k] = g.obs[t + k]
            for k in range(K):
                actions[b, k] = g.actions[t + k]
                rewards[b, k] = g.rewards[t + k]
        return {
            "obs_seq": torch.from_numpy(obs_seq),
            "actions": torch.from_numpy(actions),
            "rewards": torch.from_numpy(rewards),
        }


def distill_step(composed, optimizer, batch: dict, cfg: MuZeroConfig) -> dict[str, float]:
    """One SGD step training only g. `composed` is a DistillInitMuZeroNet
    (h + f frozen). Batch from TransitionBuffer.sample."""
    composed.train()                       # keeps h/f in eval (see DistillInitMuZeroNet.train)
    h, g, f = composed.h, composed.g, composed.f
    obs_seq = batch["obs_seq"]             # (B, K+1, 19, 8, 8)
    actions = batch["actions"]             # (B, K)
    rewards = batch["rewards"]             # (B, K)
    B, Kp1 = obs_seq.shape[0], obs_seq.shape[1]
    K = Kp1 - 1

    # Frozen teacher latents + predictions at every observation (targets).
    with torch.no_grad():
        flat = obs_seq.reshape(B * Kp1, 19, 8, 8)
        s_star = h(flat)                                   # (B*Kp1, C, 8, 8)
        tp, tv = f(s_star)                                 # teacher policy logits + value
        C = s_star.shape[1]
        s_star = s_star.reshape(B, Kp1, C, 8, 8)
        tp = tp.reshape(B, Kp1, -1)
        tv = tv.reshape(B, Kp1)
        tp_soft = torch.softmax(tp, dim=-1)                # soft policy targets

    latent_loss = obs_seq.new_zeros(())
    pred_loss = obs_seq.new_zeros(())
    reward_loss = obs_seq.new_zeros(())

    s_hat = s_star[:, 0]                                   # ŝ_0 = h(o_0) (no grad — fine, g starts at k=1)
    for k in range(K):
        s_hat, r_hat = g(s_hat, actions[:, k])
        p_hat, v_hat = f(s_hat)                            # f frozen, but grad flows to g through it
        target = k + 1
        latent_loss = latent_loss + F.mse_loss(s_hat, s_star[:, target])
        logp = F.log_softmax(p_hat, dim=-1)
        pred_loss = pred_loss + (-(tp_soft[:, target] * logp).sum(dim=-1).mean()
                                 + F.mse_loss(v_hat, tv[:, target]))
        reward_loss = reward_loss + F.mse_loss(r_hat, rewards[:, k])

    latent_loss = latent_loss / K
    pred_loss = pred_loss / K
    reward_loss = reward_loss / K
    total = (cfg.latent_loss_weight * latent_loss
             + cfg.pred_loss_weight * pred_loss
             + reward_loss)

    optimizer.zero_grad()
    total.backward()
    optimizer.step()
    return {
        "loss": float(total.item()),
        "latent_loss": float(latent_loss.item()),
        "pred_loss": float(pred_loss.item()),
        "reward_loss": float(reward_loss.item()),
    }
